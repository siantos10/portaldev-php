    <h3>Passo a Passo para Iniciantes</h3>

    <h4>Passo 1: Estrutura HTML</h4>
    <p>Crie a base do seu site com uma boa semântica:</p>
    <ul>
      <li><code>&lt;header&gt;</code> com logo e menu</li>
      <li><code>&lt;nav&gt;</code> para navegação</li>
      <li><code>&lt;main&gt;</code> com o conteúdo principal</li>
      <li><code>&lt;footer&gt;</code> com informações de copyright</li>
    </ul>

    <h4>Passo 2: Estilização com CSS</h4>
    <p>Transforme seu site visualmente:</p>
    <ul>
      <li>Defina cores, fontes e espaçamentos</li>
      <li>Use Flexbox e/ou Grid para organizar o layout</li>
      <li>Crie um design responsivo com Media Queries</li>
      <li>Adicione efeitos hover e transições suaves</li>
    </ul>

    <h4>Passo 3: Interatividade com JavaScript</h4>
    <p>Deixe seu site dinâmico:</p>
    <ul>
      <li>Menu hambúrguer para dispositivos móveis</li>
      <li>Botões com ações (ex: voltar ao topo)</li>
      <li>Validação simples de formulário</li>
      <li>Alterar conteúdo dinamicamente (ex: modo escuro)</li>
    </ul>

