        <code>&lt;main&gt;</code>, <code>&lt;footer&gt;</code>)
      </li>
      <li>
        <strong>Responsividade:</strong> Aplique Mobile First e use Media
        Queries no CSS
      </li>
      <li>
        <strong>Flexbox + Grid:</strong> Use para criar layouts modernos e
        flexíveis
      </li>
      <li>
        <strong>Interatividade:</strong> Adicione JavaScript para menus, modais,
        formulários e animações
      </li>
      <li>
        <strong>Acessibilidade:</strong> Use <code>alt</code> nas imagens e
        <code>aria-label</code> quando necessário
      </li>
      <li>
        <strong>Performance:</strong> Otimize imagens e mantenha o código limpo
      </li>
      <li>
        <strong>Consistência:</strong> Crie um Design System simples (cores,
        fontes e botões)
      </li>
      <li>
        <strong>Teste final:</strong> Verifique em diferentes dispositivos e use
        o W3C Validator
      </li>
    </ul>
